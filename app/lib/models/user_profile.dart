import 'package:hive/hive.dart';

part 'user_profile.g.dart';

@HiveType(typeId: 1)
class UserProfile extends HiveObject {
  @HiveField(0)
  final String name;
  @HiveField(1)
  final String email;
  @HiveField(2)
  final String password;
  @HiveField(3)
  final String avatarUrl;

  UserProfile({
    required this.name,
    required this.email,
    required this.password,
    required this.avatarUrl,
  });
}